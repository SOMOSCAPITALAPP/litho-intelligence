const cleanupReplacements = [
  ["Chaque étape à une fonction", "Chaque étape a une fonction"]
];

function accentize(value) {
  let next = value;
  for (const [from, to] of replacements) {
    const escaped = from.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    next = next.replace(new RegExp(`(?<![\\p{L}])${escaped}(?![\\p{L}])`, "gu"), to);
  }
  for (const [from, to] of cleanupReplacements) {
    next = next.replaceAll(from, to);
  }
  return next;
}

function walk(value, key = "") {
  if (typeof value === "string") return accentize(value);
  if (Array.isArray(value)) return value.map((item) => walk(item, key));
  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value).map(([childKey, item]) => {
        if (["id", "pdfUrl", "src"].includes(childKey)) return [childKey, item];
        return [childKey, walk(item, childKey)];
      })
    );
  }
  return value;
}

const modules = JSON.parse(fs.readFileSync(DATA_PATH, "utf8")).map((module) => {
  const polished = walk(module);
  polished.image = images[module.id];
  polished.course = polished.course.map((section) => ({
    ...section,
    bullets: bulletsByHeading[section.heading] ?? []
  }));
  return polished;
});

fs.writeFileSync(DATA_PATH, `${JSON.stringify(modules, null, 2)}\n`, "utf8");
